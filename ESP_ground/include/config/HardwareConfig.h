/**
 * @file HardwareConfig.h
 * @brief 地面站硬件引脚与串口配置集中管理
 *
 * 所有硬件引脚定义、串口号、波特率等集中在此文件。
 * 修改硬件连接时只需修改此处即可。
 */

#pragma once

#include <Arduino.h>

// ============================================================================
// 串口别名定义
// ============================================================================

/// USB 调试串口（连接 PC，用于 printf / Serial.println）
#define DebugSerial Serial

/// LoRa 无线串口（与车载 ESP32S3 通信）
#define Lora Serial1

/// 陶晶驰 TJC 串口屏
#define TJC   Serial2

// ============================================================================
// LoRa 无线串口引脚
// ============================================================================

/// LoRa RX 引脚（ESP32S3 端接收）
constexpr int LORA_RX_PIN = 18;

/// LoRa TX 引脚（ESP32S3 端发送）
constexpr int LORA_TX_PIN = 17;

// ============================================================================
// TJC 串口屏引脚
// ============================================================================

/// TJC RX 引脚（ESP32S3 端接收来自屏幕的数据）
constexpr int TJC_RX_PIN = 1;

/// TJC TX 引脚（ESP32S3 端发送数据到屏幕）
constexpr int TJC_TX_PIN = 2;

// ============================================================================
// 各串口波特率
// ============================================================================

/// USB 调试串口波特率
constexpr uint32_t DEBUG_UART_BAUD = 115200;

/// LoRa 无线串口波特率（与车载端配对一致，固定为 115200）
constexpr uint32_t LORA_UART_BAUD  = 115200;

/// TJC 串口屏波特率（与屏幕工程设置一致，固定为 115200）
constexpr uint32_t TJC_UART_BAUD   = 115200;
